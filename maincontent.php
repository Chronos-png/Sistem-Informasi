    <h2>Rekomendasi Wisata Agro Pamekasan Madura</h2>
    <div class="isi">
        <div class="paragraph">
            Pariwisata adalah salah satu subsektor yang memiliki potensi besar dalam meningkatkan perekonomian baik di tingkat nasional maupun daerah. 
            Industri jasa ini tidak hanya berpengaruh pada perekonomian lokal, tetapi juga memiliki dampak yang signifikan sebagai salah satu pendorong
            utama dalam perekonomian global yang memiliki dampak luas secara global. Dalam upaya memajukan industri pariwisata, peningkatan dalam 
            teknologi informasi menjadi suatu keharusan yang mendorong adopsi dan adaptasi terhadap perkembangan teknologi. Hal ini memberikan 
            peluang besar untuk memaksimalkan promosi destinasi pariwisata daerah. Secara umum, teknologi informasi memiliki peran penting dalam 
            penyajian informasi yang cepat, mudah, dan akurat yang sangat diinginkan oleh para wisatawan. Internet menjadi salah satu dari banyaknya
            teknologi informasi yang sangat bermanfaat bagi wisatawan, yang dapat diakses dengan mudah dari berbagai lokasi. (Palit, Talumingan, dan 
            Rumagit, 2017). Halaman web ini dibuat berdasarkan Sistem Pendukung Keputusan Rekomendasi Wisata Agro Menggunakan Metode AHP. Tujuan 
            utamanya adalah membantu para pencinta alam dalam memilih rekomendasi destinasi wisata agro di Madura. AHP, yang dikembangkan oleh
            Thomas L., adalah model pendukung keputusan yang memecah masalah kompleks dengan faktor-faktor atau kriteria multi-level menjadi
                suatu hirarki, seperti yang dijelaskan oleh Saaty (1993).
