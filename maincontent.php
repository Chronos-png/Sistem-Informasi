    Pariwisata merupakan salah satu sub sektor yang dapat meningkatkan perekonomian negara dan daerah, industri jasa ini sangat
    berpengaruh pada perekonomian lokal juga dan merupakan pendorong perekonomian dunia yang dapat memutar uang skala dunia.

    Meningkatkannya perkembangan teknologi informasi telah mewajibkan kepada industri pariwisata untuk maju dan menyesuaikan / adaptasi
    pada teknologi, sehingga menjadikan peluang promosi daerah pariwisata lebih optimal.

    Secara umum teknologi informasi akan sangat bermanfaat dalam penyajian informasi yang cepat, mudah dan akurat yang sangat dibutuhkan oleh 
    wisatawan. Salah satu dari sekian banyak teknologi informasi yang bermanfaat bagi wisatawan dan dapat diakses dengan mudah dari manapun 
    adalah internet. (Palit, Talumingan and Rumagit, 2017).

